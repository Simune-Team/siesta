        kpt[0] = ik * 0.5 / 4
        if ik == 4: kpt[0] = (ik-1) * 0.5 / 4
        for iE in xrange(100):
            E = (iE-49.5)*0.025
            for ia in xrange(dev.na_u):
                TB_dH[ia,ia] = 0.1 + 1j*np.exp(-E**2) * 1e-2
            TB_dH.save('SQUARE_dH.nc',E=E,kpt=kpt)

